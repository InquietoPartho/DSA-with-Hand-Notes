#include<iostream>
using namespace std;
int main(){
    bool a = false;
    bool b = false;
    cout<<(a&b)<<endl; // BITWISE AND
    cout<<(a|b)<<endl; // BITWISE OR
    cout<<(~a)<<endl; // BITWISE NOT
    cout<<(a&b)<<endl; // BITWISE AND
    cout<<(2&3)<<endl; // BITWISE AND on 2 numbers
    cout<<(3|4)<<endl; // BITWISE OR on 2 numbers
    int c = 12;
    cout<<c<<endl;
    return 0;
}